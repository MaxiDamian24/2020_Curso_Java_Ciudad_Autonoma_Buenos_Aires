package Clase12;

public class Arbol {
	String Nombre;
	String Apodo;
	int Altura;
	int Peso;
	String Extinto;
	
	public Arbol(String Nombre, String Apodo, int Altura, int Peso, String Extinto) {
		this.Nombre = Nombre;
		this.Apodo= Apodo;
		this.Altura = Altura;
		this.Peso = Peso;
		this.Extinto = Extinto;
	
	}

}
